// Если один из аргументов не является числом или их меньше 4 - возвращается 0. Дробные числа разрешены.

function findMaxNumber(...temps) {
    console.log(temps);
    let m = 0;
    for (let i = 0; i < temps.length; i++) {
        console.log(temps[i]);
        if (typeof(temps[i]) == 'number' && temps.length === 4) {
            m = Math.max(...temps);
        }
        else {
            return 0;
        }
    }
    return m;
}

